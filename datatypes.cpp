#include<iostream>
using namespace std;

int main()
{
    int a;
    a=12;
    float b;
    cout<<"size of float is "<<sizeof(b)<<endl;
    cout<<"size of int is "<<sizeof(a)<<endl;
    
    short int si;
    long long int li;
    cout<<"size of short int is "<<sizeof(si)<<endl;
    cout<<"size of long int is "<<sizeof(li)<<endl;

    return 0;
}